    private void CrearUsuarioBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearUsuarioBotonActionPerformed
        CrearUsu usuario = new CrearUsu();

        if(JOptionPane.showConfirmDialog(this, usuario, "Introduzca datos",
        JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
        == JOptionPane.OK_OPTION) {

        /* El primer argumento es la ventana padre (this, normalmente). El segundo argumento es el objeto recién creado, una instancia del panel. 
           El tercer argumento es el título del panel. El cuarto argumento decide qué elementos prediseñados tendrá el panel. 
           El quinto argumento refleja el tipo del panel: de error, de aviso, normal, etc.) */

        /* El método devuelve la opción escogida por el usuario (Aceptar o Cancelar). Por tanto, aquí va el código que se ejecutará si se pulsa OK en el panel.*/

        //Recogida datos  
        if(!usuario.getNombre().equals("") && !usuario.getContraseña().equals("")){
            String nombreUsu = usuario.getNombre();
            String contraseñaUsu = usuario.getContraseña();
            Usuario usu = new Usuario(nombreUsu, contraseñaUsu);
            usuarios.registrar(usu);// TODO add your handling code here:
            UltimaOperacion.setText ("Ultima operacion: usuario creado");
//            clasificacion.AñadirUsu(usu);
            this.usuario = usu;
            NombreUsuarioText.setText(usu.toString());
        }
        else
            UltimaOperacion.setText("El usuario introducido es nulo");
        NombreUsuarioText.setEditable(false);
        }
    }//GEN-LAST:event_CrearUsuarioBotonActionPerformed

    private void IniciarSesionBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniciarSesionBotonActionPerformed
        IniciarSesion sesion = new IniciarSesion();
        
        if(JOptionPane.showConfirmDialog(this, sesion, "Introduzca datos",
        JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
        == JOptionPane.OK_OPTION) {
        
        String nombreUsu = sesion.getNombre();
        String contraseñaUsu = sesion.getContraseña();
        
        try{
            Usuario usu = new Usuario(nombreUsu,contraseñaUsu);
            this.usuarios.autenticar(usu);
            this.usuario = usu;
            NombreUsuarioText.setText(usu.toString());
            UltimaOperacion.setText ("Ultima operacion: sesion iniciada");
            
        }
        catch(UsuarioNoEncontradoException e1){
            UltimaOperacion.setText ("Nombre de usuario / contraseña incorrecto(s)");
        }
        }
    }//GEN-LAST:event_IniciarSesionBotonActionPerformed

